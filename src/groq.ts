import { Env, GroqWhisperResponse, GroqChatResponse } from "./types";

const GROQ_API_BASE = "https://api.groq.com/openai/v1";

export async function transcribeVoice(
  env: Env,
  audioBuffer: ArrayBuffer,
  filename: string
): Promise<string> {
  const formData = new FormData();
  formData.append("file", new Blob([audioBuffer], { type: "audio/ogg" }), filename);
  formData.append("model", "whisper-large-v3-turbo");
  formData.append("language", "ru");
  formData.append("response_format", "json");

  const res = await fetch(`${GROQ_API_BASE}/audio/transcriptions`, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${env.GROQ_API_KEY}`,
    },
    body: formData,
  });

  if (!res.ok) {
    const err = await res.text();
    throw new Error(`Groq Whisper error: ${res.status} ${err}`);
  }

  const data: GroqWhisperResponse = await res.json();
  return data.text;
}

export async function generateComplaint(
  env: Env,
  target: string,
  userDescription: string
): Promise<string> {
  const systemPrompt = getSystemPrompt(target);

  const res = await fetch(`${GROQ_API_BASE}/chat/completions`, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${env.GROQ_API_KEY}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      model: "llama-3.3-70b-versatile",
      messages: [
        { role: "system", content: systemPrompt },
        { role: "user", content: userDescription },
      ],
      temperature: 0.3,
      max_tokens: 4096,
    }),
  });

  if (!res.ok) {
    const err = await res.text();
    throw new Error(`Groq LLM error: ${res.status} ${err}`);
  }

  const data: GroqChatResponse = await res.json();
  return data.choices[0].message.content;
}

